name = "Mohamed Ibrahim"  # Single Word => Normal
myName = "Mohamed Ibrahim"  # Two Words => camelCase
my_name = "Mohamed Ibrahim"  # Two Words => snake_case

print(name)
print(myName)
print(my_name)